      const en = document.getElementById('content-en');
      if (!de || !en) return;
      de.style.display = l === 'de' ? '' : 'none';
      en.style.display = l === 'en' ? '' : 'none';
    }
    updatePageContent();
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.addEventListener('click', () => setTimeout(updatePageContent, 10));
    });
